func TestIsLessThanHalfL(t *testing.T) {
	tests := []struct {
		name string
		s    []byte
		want bool
	}{
		{
			name: "zero",
			s:    make([]byte, 32),
			want: true,
		},
		{
			name: "one",
			s: func() []byte {
				s := make([]byte, 32)
				s[0] = 1
				return s
			}(),
			want: true,
		},
		{
			name: "halfL_minus_one",
			s: func() []byte {
				s := make([]byte, 32)
				copy(s, halfL)
				s[0]-- // Subtract 1
				return s
			}(),
			want: true,
		},
		{
			name: "halfL_exact",
			s:    halfL,
			want: false,
		},
		{
			name: "halfL_plus_one",
			s: func() []byte {
				s := make([]byte, 32)
				copy(s, halfL)
				s[0]++ // Add 1
				return s
			}(),
			want: false,
		},
		{
			name: "max_value",
			s: func() []byte {
				s := make([]byte, 32)
				for i := range s {
					s[i] = 0xff
				}
				return s
			}(),
			want: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := isLessThanHalfL(tt.s)
			if got != tt.want {
				t.Errorf("isLessThanHalfL() = %v, want %v", got, tt.want)
				t.Logf("S:     %x", tt.s)
				t.Logf("halfL: %x", halfL)
			}
		})
	}
}
